    public void getTableDATA()
    {
        int i=0,j=0;
        MasterSrvs msrv=new MasterSrvs();
        ReasonModel rm=null;
        List lst=new LinkedList();
        lst=(List)msrv.getReasonTab();
        Object dat[][]=new Object[lst.size()][4];
        String col[]={"ಕ್ರಂ ಸಂಖ್ಯೆ","ಕಾರಣ"};
        Iterator itm=lst.iterator();
        while(itm.hasNext())
        {
            rm=(ReasonModel)itm.next();
            dat[i][0]=(String)rm.getSlno();
            dat[i][1]=(String)rm.getReason();
            i++;
        }
       DefaultTableModel dm=new DefaultTableModel(dat,col);
       rtab.setModel(dm);
       rtab.setAutoResizeMode(rtab.AUTO_RESIZE_OFF);
       rtab.getColumnModel().getColumn(0).setPreferredWidth(80);
       rtab.getColumnModel().getColumn(1).setPreferredWidth(590);
    }
