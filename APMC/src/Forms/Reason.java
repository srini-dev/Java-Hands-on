    private void rtabKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rtabKeyPressed
        // TODO add your handling code here:
        int k=evt.getKeyCode();
        if(k==10) {
            try{
                int ro=rtab.getSelectedRow();
                t1=rtab.getModel().getValueAt(ro,0).toString();
                MasterSrvs msrv=new MasterSrvs();
                ReasonModel rm=null;
                rm=(ReasonModel)msrv.getRBsno(t1);
                txt1.setText(rm.getSlno());
                txt2.setText(rm.getReason());
                updatebtn.setEnabled(true);
                savebtn.setEnabled(false);}catch(Exception e){}
        }
        if(k==java.awt.event.KeyEvent.VK_UP)//-1
        {
            try{
                int ro=rtab.getSelectedRow();
                t1=rtab.getModel().getValueAt(ro-1,0).toString();
                MasterSrvs msrv=new MasterSrvs();
                ReasonModel rm=null;
                rm=(ReasonModel)msrv.getRBsno(t1);
                txt1.setText(rm.getSlno());
                txt2.setText(rm.getReason());
                updatebtn.setEnabled(true);
                savebtn.setEnabled(false);}catch(Exception e){}

        }
        if(k==java.awt.event.KeyEvent.VK_DOWN)//+1
        {
            try{
                int ro=rtab.getSelectedRow();
                t1=rtab.getModel().getValueAt(ro+1,0).toString();
                MasterSrvs msrv=new MasterSrvs();
                ReasonModel rm=null;
                rm=(ReasonModel)msrv.getRBsno(t1);
                txt1.setText(rm.getSlno());
                txt2.setText(rm.getReason());
                updatebtn.setEnabled(true);
                savebtn.setEnabled(false);}catch(Exception e){}

        }
}//GEN-LAST:event_rtabKeyPressed

