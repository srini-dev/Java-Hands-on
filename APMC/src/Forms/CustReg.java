    public void getTableDATA()
    {
        int i=0,j=0;
        CustomerSrvs csr=new CustomerSrvs();
        CstModel cstm=null;
        List lst=new LinkedList();
        lst=(List)csr.getcstTab();
        Object dat[][]=new Object[lst.size()][13];
        String col[]={"ಖಾತೆ ಸಂಖ್ಯೆ","ಹೆಸರು","ಊರು","ಮೊಬೈಲ್‍ ಸಂಖ್ಯೆ"};
        Iterator itm=lst.iterator();
        while(itm.hasNext())
        {
            cstm=(CstModel)itm.next();
            dat[i][0]=(String)cstm.getCid();
            dat[i][1]=(String)cstm.getCnam();
            dat[i][2]=(String)cstm.getCtown();
            dat[i][3]=(String)cstm.getCphno();
            i++;
        }
       DefaultTableModel dm=new DefaultTableModel(dat,col);
       csttab.setModel(dm);
       csttab.setAutoResizeMode(csttab.AUTO_RESIZE_OFF);
       csttab.getColumnModel().getColumn(0).setPreferredWidth(90);
       csttab.getColumnModel().getColumn(1).setPreferredWidth(130);
       csttab.getColumnModel().getColumn(2).setPreferredWidth(150);
       csttab.getColumnModel().getColumn(3).setPreferredWidth(100);
    }
