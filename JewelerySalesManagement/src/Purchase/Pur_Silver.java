    private void pmtabKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pmtabKeyPressed

        int keycode=evt.getKeyCode();
        if(keycode==10) {
            int ro=pmtab.getSelectedRow();
            String pid=pmtab.getModel().getValueAt(ro,0).toString();
            String pmod=pmtab.getModel().getValueAt(ro,5).toString();
            System.out.println("OOOOOOOOO"+pid);

            //String pmod=gpmt6.getSelectedItem().toString();
            if(pmod.equals("By Cash") || gpmt6.getSelectedItem().equals("By Cash")) {

                pbtnsave.setEnabled(false);
                gpmt6.setSelectedItem(pmod);
                pcbtnmod.setEnabled(true);

                try{

                    if(gpmt1.getText().length()==0)JOptionPane.showMessageDialog(null,"Voucher No Shoud not be blank");
                    String vch=gpmt1.getText().trim();
                    PurchaseServices ps=new PurchaseServices();
                    Payment  pm=(Payment)ps.getPayItmsFromTab(vch, pid);
                    byCashSetTrue();
                    byBankSetFalse();

                    gpmt2.setText(String.valueOf(pm.getPid()));
                    gpmt5.setText(String.valueOf(pm.getVbal()));
                    gpmt7.setText(String.valueOf(pm.getPamt()));
                    String cdat=String.valueOf(pm.getPdat());
                    String dts=ToDateFormat.DToD(cdat);
                    Date d=new Date(dts);
                    gpmt8.setDate(d);
                    gpmt9.setText(String.valueOf(pm.getBprsn()));
                    gpmt7.requestFocus(true);
                }catch(Exception e){}
            } else if(pmod.equals("ByBank") || gpmt6.getSelectedItem().equals("ByBank")) {
                pbtnsave.setEnabled(false);
                pbbtnmod.setEnabled(true);
                try{
                    if(gpmt1.getText().length()==0)JOptionPane.showMessageDialog(null,"Voucher No Shoud not be blank");
                    String vch=gpmt1.getText().trim();
                    PurchaseServices ps=new PurchaseServices();
                    Payment  pm=(Payment)ps.getPayItmsFromTab(vch, pid);

                    byBankSetTrue();
                    gpmt6.setSelectedItem(pmod);
                    byCashSetFalse();

                    gpmt2.setText(String.valueOf(pm.getPid()));

                    String bdat=String.valueOf(pm.getPdat());
                    String dts=ToDateFormat.DToD(bdat);
                    Date d=new Date(dts);
                    gpmt11.setDate(d);

                    gpmt5.setText(String.valueOf(pm.getVbal()));
                    gpmt10.setText(String.valueOf(pm.getPamt()));
                    String rd=String.valueOf(pm.getByt());
                    if(rd.equals("Cheque"))
                        gpmt12.setSelected(true);
                    if(rd.equals("DD"))
                        gpmt13.setSelected(true);
                    if(rd.equals("RTGS"))
                        gpmt14.setSelected(true);

                    gpmt15.setText(String.valueOf(pm.getLno()));
                    gpmt16.setText(String.valueOf(pm.getActno()));
                    gpmt17.setText(String.valueOf(pm.getActhldr()));
                    gpmt18.setSelectedItem(String.valueOf(pm.getBnam()));
                    gpmt19.setSelectedItem(String.valueOf(pm.getBbrch()));


                    gpmt10.requestFocus(true);
                }catch(Exception e){}

            } else {
                JOptionPane.showMessageDialog(null, "Please select Payement Mode fisrt");}
        }
    }//GEN-LAST:event_pmtabKeyPressed

